        public static string GetRandomText()
        {
            var random = new Random();
            var texts = GetTexts();
            var text = texts[random.Next(texts.Length)];

            // Replaces newlines with spaces and collapses multiple spaces into a single space
            text = System.Text.RegularExpressions.Regex.Replace(text, @"\s+", " ").Trim();

            // Trim the text of whitespace on both ends
            text = text.Trim();
            return text;
        }
